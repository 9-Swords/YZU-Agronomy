#PM2.5     空气质量等级
#=======================
#0-35      优
#35-75     良
#75-115    轻度污染
#115-150   中度污染
#150-250   重度污染
#250-500   严重污染
#======================
PM25=eval(input("请输入PM25的值："))
if PM25<35:
    print("空气质量优")
elif PM25<75:
    print("空气质量良")
elif PM25<115:               
    print("空气轻度污染")
elif PM25<150:
    print("空气中度污染")
elif PM25<250:
    print("空气重度污染")           
elif PM25<500:
    print("空气严重污染")
